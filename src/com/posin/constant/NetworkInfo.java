package com.posin.constant;

public class NetworkInfo {

	public String wlan0_ip;
	public String wlan0_mac;
	public String eth0_ip;
	public String eth0_mac;

	public String getWlan0_ip() {
		return wlan0_ip;
	}

	public void setWlan0_ip(String wlan0_ip) {
		this.wlan0_ip = wlan0_ip;
	}

	public String getWlan0_mac() {
		return wlan0_mac;
	}

	public void setWlan0_mac(String wlan0_mac) {
		this.wlan0_mac = wlan0_mac;
	}

	public String getEth0_ip() {
		return eth0_ip;
	}

	public void setEth0_ip(String eth0_ip) {
		this.eth0_ip = eth0_ip;
	}

	public String getEth0_mac() {
		return eth0_mac;
	}

	public void setEth0_mac(String eth0_mac) {
		this.eth0_mac = eth0_mac;
	}

}
